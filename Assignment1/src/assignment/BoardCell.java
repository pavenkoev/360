/**
 * Enum representing the possible states of a cell in a game board.
 * Each cell in the board can be either empty or contain a mine.
 * @author Elle Pavenko
 * @version 1.0
 */
public enum BoardCell {
    /** Represents an empty cell, which does not contain a mine. */
    Empty,
    /** Represents a cell that contains a mine. */
    Mine
}
